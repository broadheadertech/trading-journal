export function seedDataIfEmpty() {
  const SEED_KEY = 'crypto-journal-seeded-v3';
  if (typeof window === 'undefined') return;
  if (localStorage.getItem(SEED_KEY)) return;

  const existingTrades = localStorage.getItem('crypto-journal-trades');
  if (existingTrades && JSON.parse(existingTrades).length > 0) return;

  localStorage.setItem('crypto-journal-trades', JSON.stringify(sampleTrades));
  localStorage.setItem('crypto-journal-strategies', JSON.stringify(sampleStrategies));
  localStorage.setItem('crypto-journal-checklists', JSON.stringify(sampleChecklists));
  localStorage.setItem('crypto-journal-journal', JSON.stringify(sampleJournal));
  localStorage.setItem('crypto-journal-breaker-events', JSON.stringify(sampleBreakerEvents));
  localStorage.setItem('crypto-journal-triggers', JSON.stringify(sampleTriggers));
  localStorage.setItem(SEED_KEY, 'true');
}
